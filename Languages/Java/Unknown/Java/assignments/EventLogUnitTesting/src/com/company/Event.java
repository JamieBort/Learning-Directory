package com.company;


// Write an Event JavaBean with
// String name
// String action
public class Event {
    String name;
    String action;

    public Event() {
        this.name = name;
        this.action = action;
    }

    public java.lang.String getName() {
        return name;
    }

    public void setName(java.lang.String name) {
        this.name = name;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }
}
